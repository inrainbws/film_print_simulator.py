# Translation helper function
def tr(key, lang_code="en"):
    """Get translated text for a key in the specified language"""
    if key in TRANSLATIONS and lang_code in TRANSLATIONS[key]:
        return TRANSLATIONS[key][lang_code]
    # Fallback to English if translation not found
    if key in TRANSLATIONS and "en" in TRANSLATIONS[key]:
        return TRANSLATIONS[key]["en"]
    # Return the key itself as last resort
    return key
